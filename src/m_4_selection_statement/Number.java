package m_4_selection_statement;

public class Number {
    public static void main(String[] args) {

        int number=3;

        if(number>0){
            System.out.println("Number is positive");
        } else if (number<0) {
            System.out.println("Number is negative");
        }else {
            System.out.println("Number is zero");
        }


    }
}
