package m2_variable_and_data_types;

public class CharVariables {
    public static void main(String[] args) {

        char a='a';
        char queationMark='?';

        System.out.println(a);
        System.out.println(queationMark);

        char single='\'';
        System.out.println(single);

        char A='A';
        char asciiCode=65;
        System.out.println(asciiCode);

        char openingBrace=0x28;
        System.out.println(openingBrace);

        char unicode='\u0041';
        System.out.println(unicode);



    }
}
