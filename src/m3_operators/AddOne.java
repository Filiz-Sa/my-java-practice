package m3_operators;
/*
Declare and initialize num1 and num2 variables with max value of int and long.
Increase each variables value by one then print the variables name and their latest values.
 */
public class AddOne {
    public static void main(String[] args) {
        int maxInt=Integer.MAX_VALUE;
        long maxLong= Long.MAX_VALUE;

        System.out.println(maxInt);
        System.out.println(maxLong);

        System.out.println(maxInt + 1);
        System.out.println(maxLong + 1);




    }
}
